'''
Cоздание пакета калькулятора с модулями (функциями)
'''

def add(a, b):
    return print("Результат сложения:", a + b)

def minus(a, b):
    return print("Результат вычитания:", a -b)

def multy(a, b):
    return print("Результат умножения:", a * b)

def divide(a, b):
    return print("Результат деления:", a / b)

def multyply(a, b):
    return print("Результат возведения в степень:", a ** b)
