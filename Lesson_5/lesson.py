'''
Словари — это коллекции пар "ключ-значение", где ключи уникальны, а значения могут быть любыми типами данных.
Словари очень полезны для хранения ассоциированных данных.
'''

my_dict = {"name": "John", "age": 25}
my_dict["age"] = 26  # изменение значения по ключу

print("Словарь:", my_dict)
