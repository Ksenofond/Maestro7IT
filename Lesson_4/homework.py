# Создаем автора
tolkien = Author("Дж. Р. Р. Толкин", 1892)
lotr = tolkien.write_book("Властелин колец", 1954, "Фэнтези")
hobbit = tolkien.write_book("Хоббит", 1937, "Фэнтези")

# Создаем библиотеку
my_library = Library("Центральная библиотека")

# Создаем библиотекаря
librarian = Librarian("Екатерина")

# Добавляем книги в библиотеку
librarian.add_book(my_library, lotr)
librarian.add_book(my_library, hobbit)

# Выводим список книг в библиотеке
print("\nКниги в библиотеке:")
my_library.list_books()

# Создаем члена библиотеки
john = Member("Иван")

# Член библиотеки берет книгу
john.borrow_book(lotr)

# Проверка состояния книги
librarian.check_availability(lotr)

# Выводим список книг, взятых Иваном
print("\nКниги, взятые Иваном:")
print(john.get_borrowed_books())

# Член возвращает книгу
john.return_book(lotr)

# Проверка состояния книги после возврата
librarian.check_availability(lotr)

# Список книг в библиотеке снова
print("\nКниги в библиотеке:")
my_library.list_books()

