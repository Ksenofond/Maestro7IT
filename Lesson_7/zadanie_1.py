# Определяем значения переменных
# a = 2
# b = 3
# c = 4
# d = 5

# Запрашиваем значения у пользователя и преобразуем их в числа
a = int(input("Введите значение a: "))
b = int(input("Введите значение b: "))
c = int(input("Введите значение c: "))
d = int(input("Введите значение d: "))

# Выражение по формуле
result = (a * b) / (c * d) - (a * b - c) / (c * d)

# Выводим результат
print("\nЗначение выражения:", result)
print("Выполнила: Егорова К.П.")
