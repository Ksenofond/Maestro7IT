'''
Заданы координаты трех вершин треугольника (x1, y1), (х2, y2), (x3, y3).
Найти его периметр и площадь.
'''

import math

# Вводим координаты трех вершин треугольника
x1, y1 = map(float, input("Введите координаты первой вершины (x1 y1): ").split())
x2, y2 = map(float, input("Введите координаты второй вершины (x2 y2): ").split())
x3, y3 = map(float, input("Введите координаты третьей вершины (x3 y3): ").split())

# Вычисление длины сторон
AB = math.sqrt((x2 - x1)**2 + (y2 - y1)**2)
BC = math.sqrt((x3 - x2)**2 + (y3 - y2)**2)
CA = math.sqrt((x3 - x1)**2 + (y3 - y1)**2)

# Периметр
perimeter = AB + BC + CA

# Площадь
area = 0.5 * abs(x1 * (y2 - y3) + x2 * (y3 - y1) + x3 * (y1 - y2))

# Показываем формулы
print("\nФормула периметра: AB + BC + CA")
print("Формула площади: 0.5 * abs(x1 * (y2 - y3) + x2 * (y3 - y1) + x3 * (y1 - y2))")

# Выводим результаты
print(f"\nПериметр треугольника: {perimeter:.2f}")
print(f"Площадь треугольника: {area:.2f}")
print("\nВыполнила: Егорова К.П.")
